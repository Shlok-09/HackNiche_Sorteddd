export default function Home() {
  const [loopNum, setLoopNum] = useState(0);
  const [isDeleting, setIsDeleting] = useState(false);
  const [text, setText] = useState('');
  const [delta, setDelta] = useState(300 - Math.random() * 100);
  const [index, setIndex] = useState(1);
  const toRotate = [ "Savings", "Insurances", "Policies" ];
  const period = 2000;

  useEffect(() => {
    let ticker = setInterval(() => {
      tick();
    }, delta);

    return () => { clearInterval(ticker) };
  }, [text])

  const tick = () => {
    let i = loopNum % toRotate.length;
    let fullText = toRotate[i];
    let updatedText = isDeleting ? fullText.substring(0, text.length - 1) : fullText.substring(0, text.length + 1);

    setText(updatedText);

    if (isDeleting) {
      setDelta(prevDelta => prevDelta / 2);
    }

    if (!isDeleting && updatedText === fullText) {
      setIsDeleting(true);
      setIndex(prevIndex => prevIndex - 1);
      setDelta(period);
    } else if (isDeleting && updatedText === '') {
      setIsDeleting(false);
      setLoopNum(loopNum + 1);
      setIndex(1);
      setDelta(500);
    } else {
      setIndex(prevIndex => prevIndex + 1);
    }
  }
  return (
    <Wrapper className='h-screen'>
      <Spline scene="https://prod.spline.design/NLaYeZL3WV8muZ0Z/scene.splinecode" />
      <Social>
      </Social>
      <Content>
        <Menu>
          <li>
          </li>
          <li>
            <a href="/">Home</a>
          </li>
          <li>
          <a href="/">Login</a>
          </li>
          <li>
            <button>Get Started</button>
          </li>
        </Menu>
        <TrackVisibility>
            {({ isVisible }) =>
            <div className={isVisible ? "animate__animated animate__fadeIn" : ""}>
              <h1>{`Solutions to`} <span className="txt-rotate" dataperiod="1000" data-rotate='[ "Savings", "Insurances", "Policies" ]'><span className="wrap tracking-tight">{text}</span></span></h1>
                <p>SecurePocket.ai is a solution to all your financial support. Login And track all your expenses and get suggestions which is the perfect one for you.</p>
            </div>}
          </TrackVisibility>
        {/* <h1>Collaborate with people</h1>
        <p>
          Bring your team together and build your community by using our
          cross-platform app that lets you collaborate via chat, voice and by
          sharing and storing unlimited media files. A world of topics is
          waiting for you. Join the private beta.
        </p> */}
        <button className='p-6 bg-secondary-dark-bg text-white'>
          Download Our App
        </button>
      </Content>
    </Wrapper>
    
  );
}

